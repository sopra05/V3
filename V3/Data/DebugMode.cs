namespace V3.Data
{
    /// <summary>
    /// A debug level that can be set by the player.
    /// </summary>
    public enum DebugMode
    {
        /// <summary>
        /// Disable all debug utils.
        /// </summary>
        Off,
        /// <summary>
        /// Show the FPS counter.
        /// </summary>
        Fps,
        /// <summary>
        /// Show all debug information.
        /// </summary>
        Full
    }
}
